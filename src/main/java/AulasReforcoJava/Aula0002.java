        h = 5.0;

        area = (b + B) / 2.0 * h;

        System.out.printf("A área do Trapézio é de : %.2f", area);


        //--

        int aa, bb, cc, dd;

        double resultado, result;

        aa = 5;
        bb = 2;
        cc = 2;
        dd = 3;

        resultado = (double) aa / bb;
        result = (double) cc / dd;

        System.out.println("\n");
        System.out.println(resultado);
        System.out.println(result);

